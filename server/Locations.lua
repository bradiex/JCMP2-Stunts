	local posY = math.clamp(math.floor((pos.z+20000)/40000*32), 1, 32)
	--print(math.ceil((pos.x+20000)) .. " " .. math.ceil((pos.z+20000)))
	--print(posX .. " " .. posY)
	local locID = Locations.raster[posY][posX]
	return Locations.names[locID]
end